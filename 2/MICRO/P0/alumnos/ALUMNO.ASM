;_______________________________________________________________
; DEFINICION DEL SEGMENTO DE DATOS
DATOS SEGMENT
	CLR_PANT 	DB 	1BH,"[2","J$"
	CUR_MICRO	DB 	1BH,"4;27fTECNOLOGIA DE COMPUTADORES$"
	PREG_NOM 	DB 	1BH,"[15;1fESCRIBE TU NOMBRE: $"
	NOMBRE		DB 	80 DUP (0)
	TEXTO 		DB 	1BH,"[22;1fDON ","$"
	TEXTO2 		DB 	" ES ALUMNO DE 2º CURSO DE "
				DB 	"INGENIERIA INFORMATICA",13,10,"$"
	PREG_AP1 	DB 	1BH,"[15;1fESCRIBE TU PRIMER APELLLIDO: $"		;LAS NUEVAS VARIABLES AL FINAL DEL SEGMENTO
	PREG_AP2 	DB 	1BH,"[15;1fESCRIBE TU SEGUNDO APELLIDO: $"
	AP1			DB 	80 DUP (0)
	AP2			DB 	80 DUP (0)
	SPACE 		DB 	1BH," "
	
DATOS ENDS
;_______________________________________________________________
; DEFINICION DEL SEGMENTO DE PILA
PILA SEGMENT STACK "STACK"
	DB 40H DUP (0)
PILA ENDS
;_______________________________________________________________
; DEFINICION DEL SEGMENTO DE CODIGO
CODE SEGMENT
ASSUME CS:CODE,DS:DATOS,SS:PILA
; COMIENZO DEL PROCEDIMIENTO PRINCIPAL (START)
START PROC FAR
;INICIALIZACION DE LOS REGISTROS DE SEGMENTO
	MOV AX,DATOS
	MOV DS,AX	;FIN DE LAS INICIALIZACIONES
	
	MOV AH,9	; BORRA LA PANTALLA
	MOV DX, OFFSET CLR_PANT
	INT 21H
	
	MOV DX,OFFSET CUR_MICRO		;ESCRIBE "TECNOLOGIA DE COMPUTADORES"
	INT 21H

	MOV DX,OFFSET PREG_NOM		;MUESTRA "ESCRIBE TU NOMBRE: ...
	INT 21H
                   
	MOV AH,0AH			;ALMACENA EL NOMBRE TECLEADO
	MOV DX,OFFSET NOMBRE
	
	MOV NOMBRE[0],60		;MAXIMO NUMERO DE CARACTERES
	INT 21H
	
	MOV AH,9	; BORRA LA PANTALLA
	MOV DX, OFFSET CLR_PANT
	INT 21H
	
	MOV DX,OFFSET PREG_AP1		;MUESTRA "ESCRIBE TU AP1: ...
	INT 21H
                   
	MOV AH,0AH			;ALMACENA EL APELLIDO TECLEADO
	MOV DX,OFFSET AP1
	
	MOV AP1[0],60		;MAXIMO NUMERO DE CARACTERES
	INT 21H
	
	MOV AH,9	; BORRA LA PANTALLA
	MOV DX, OFFSET CLR_PANT
	INT 21H
	
	MOV DX,OFFSET PREG_AP2		;MUESTRA "ESCRIBE TU AP2: ...
	INT 21H
                   
	MOV AH,0AH			;ALMACENA EL SEGUNDO APELLIDO TECLEADO
	MOV DX,OFFSET AP2
	
	MOV AP2[0],60		;MAXIMO NUMERO DE CARACTERES
	INT 21H

	MOV AH,9			;ESCRIBE "DON ...
	MOV DX, OFFSET TEXTO
	INT 21H

moV BX,0 			;ACTUALIZA EL CONTADOR DEL BUCLE
OTRO:
	MOV DL,NOMBRE[BX+2]		;ESCRIBIR UN CARACTER DEL NOMBRE
	MOV AH,2
	INT 21H
	
	INC BX
	CMP BL, NOMBRE[1]		;COMPARA CON EL NUMERO DE CARACTERES
	JNE OTRO
	
moV BX,0
OTRO1:	
	MOV DL,AP1[BX+2]		;ESCRIBIR UN CARACTER DEL AP1
	MOV AH,2
	INT 21H
	
	INC BX
	CMP BL, AP1[1]		;COMPARA CON EL NUMERO DE CARACTERES
	JNE OTRO1

moV BX,0	
OTRO2:	
	MOV DL,AP2[BX+2]		;ESCRIBIR UN CARACTER DEL AP2
	MOV AH,2
	INT 21H
	
	INC BX
	CMP BL, AP2[1]		;COMPARA CON EL NUMERO DE CARACTERES
	JNE OTRO2

	MOV DX, OFFSET TEXTO2		; ESCRIBE "ES ALUMNO ...
	MOV AH,9
	INT 21H
	
	MOV AX,4C00H			; FIN DE PROGRAMA Y VUELTA AL DOS
	INT 21H
START ENDP
;FIN DEL SEGMENTO DE CODIGO
CODE ENDS
;FIN DE PROGRAMA INDICANDO DONDE COMIENZA LA EJECUCION
END START

